public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10, 10));
    }

    /*
    INI KOMENTAR MULTILINE UNTUK DOKUMENTASI (BISA DITAMBAHKAN SEBANYAK BANYAKNYA)
    GQQGQQ2RQ
    Q2EQ2EQ2EQ
    AQQ1QWDQ
    AWDQDFQ
     */

    /**
     * METHOD UNTUK MENJUMLAHKAN value1 DITAMBAH value2
     * @param value1
     * @param value2
     * @return
     */
    static int sum(int value1, int value2){
        // jumlahkan value1 ditambah value2
        return value1 + value2;
    }
}
